package org.itst.domain;

public class ClubMember {
	private String name;
	private String stuId;
	private String sex;
	private String fromAcademy;
	private String fromClubName;
	private String phoneNumber;
	private String position;
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStuId() {
		return stuId;
	}
	public void setStuId(String stuId) {
		this.stuId = stuId;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getFromAcademy() {
		return fromAcademy;
	}
	public void setFromAcademy(String fromAcademy) {
		this.fromAcademy = fromAcademy;
	}
	public String getFromClubName() {
		return fromClubName;
	}
	public void setFromClubName(String fromClubName) {
		this.fromClubName = fromClubName;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
}
