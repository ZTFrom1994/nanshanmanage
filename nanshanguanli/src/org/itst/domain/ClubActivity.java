package org.itst.domain;

public class ClubActivity {
	private String activityName;
	private String activityStartTime;
	private String activityEndTime;
	private String activityFromClub;
	private String activityLeaderName;
	private String activityHoldPlace;
	private String activityContact;
	private String activityForm;
	private String isBorrowed;
	private String activityIntroduction;
	private String activityId;
	public String getActivityName() {
		return activityName;
	}
	public void setActivityName(String activityName) {
		this.activityName = activityName;
	}
	public String getActivityStartTime() {
		return activityStartTime;
	}
	public void setActivityStartTime(String activityStartTime) {
		this.activityStartTime = activityStartTime;
	}
	public String getActivityEndTime() {
		return activityEndTime;
	}
	public void setActivityEndTime(String activityEndTime) {
		this.activityEndTime = activityEndTime;
	}
	public String getActivityFromClub() {
		return activityFromClub;
	}
	public void setActivityFromClub(String activityFromClub) {
		this.activityFromClub = activityFromClub;
	}
	public String getActivityLeaderName() {
		return activityLeaderName;
	}
	public void setActivityLeaderName(String activityLeaderName) {
		this.activityLeaderName = activityLeaderName;
	}
	public String getActivityHoldPlace() {
		return activityHoldPlace;
	}
	public void setActivityHoldPlace(String activityHoldPlace) {
		this.activityHoldPlace = activityHoldPlace;
	}
	public String getActivityContact() {
		return activityContact;
	}
	public void setActivityContact(String activityContact) {
		this.activityContact = activityContact;
	}
	public String getActivityForm() {
		return activityForm;
	}
	public void setActivityForm(String activityForm) {
		this.activityForm = activityForm;
	}
	public String getIsBorrowed() {
		return isBorrowed;
	}
	public void setIsBorrowed(String isBorrowed) {
		this.isBorrowed = isBorrowed;
	}
	public String getActivityIntroduction() {
		return activityIntroduction;
	}
	public void setActivityIntroduction(String activityIntroduction) {
		this.activityIntroduction = activityIntroduction;
	}
	public String getActivityId() {
		return activityId;
	}
	public void setActivityId(String activityId) {
		this.activityId = activityId;
	}
}
